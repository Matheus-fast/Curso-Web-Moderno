/**
 * Operação que utiliza apenas um operando 
 * 
 * Operadores:
 * 1. + - Converte o operando em um número
 * 2. - - Converte o operando em um número e nega seu valor posteriormente
 * 3. ++ - Adiciona um valor ao operando
 * 4. -- - Subtrai um valor do operando
*/

let num1 = 1
let num2 = 2

num1++ 
